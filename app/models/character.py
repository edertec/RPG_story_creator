class Character:
    def __init__(self, name, level, char_class, subclass, background, race, personality, objective, alignment, age, skin, height, eyes, weight, other_features):
        self.name = name
        self.level = level
        self.char_class = char_class
        self.subclass = subclass
        self.background = background
        self.race = race
        self.personality = personality
        self.objective = objective
        self.alignment = alignment
        self.age = age
        self.skin = skin
        self.height = height
        self.eyes = eyes
        self.weight = weight
        self.other_features = other_features
